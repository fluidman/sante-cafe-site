	function updateTimepickerLimits() {
	  const dateStr = $('.appointment_date').val();
	  const timeInput = $('.appointment_time');

	  if (!dateStr) return;

	  const date = new Date(dateStr);
	  const day = date.getDay(); // Sunday = 0, Monday = 1, ..., Saturday = 6

	  // Base settings
	  let minTime = '07:00';
	  let maxTime;
	  let isClosed = false;

	  switch (day) {
		case 0: // Sunday
		  maxTime = '13:30';
		  break;
		case 5: // Friday
		  maxTime = '14:30';
		  break;
		case 6: // Saturday
		  isClosed = true;
		  break;
		default: // Monday to Thursday
		  maxTime = '16:30';
	  }

	  if (isClosed) {
		timeInput.timepicker('remove');
		timeInput.val('Samstags geschlossen');
		timeInput.prop('disabled', true);
	  } else {
		timeInput.prop('disabled', false);
		timeInput.val('');
		timeInput.timepicker('remove');
		timeInput.timepicker({
		  timeFormat: 'H:i',
		  interval: 30,
		  minTime: minTime,
		  maxTime: maxTime,
		  startTime: minTime,
		  dynamic: false,
		  dropdown: true,
		  scrollbar: true
		});
	  }
	}

